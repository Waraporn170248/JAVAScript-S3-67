for (let char of "test") {
    // วนลูป `for...of` เพื่อวนผ่านตัวอักษรในสตริง "test"
    // ตัวลูปจะทำงาน 4 ครั้ง: หนึ่งครั้งสำหรับตัวอักษรแต่ละตัวในสตริง

    console.log( char ); // t, then e, then s, then t
    // แสดงค่าของตัวอักษรในแต่ละรอบของลูป:
    // รอบที่ 1: 't'
    // รอบที่ 2: 'e'
    // รอบที่ 3: 's'
    // รอบที่ 4: 't'

}